import type { AirDocument } from "@anvil/air";

// The live MCP server now lives in @anvil/mcp-runtime (the thin serving path).
// Re-exported here so build-time callers (tests, `anvil serve`) keep one import
// site, while the *generated* deployed server depends on @anvil/mcp-runtime
// directly and never pulls in this artifact foundry.
export {
  buildMcpServer,
  MCP_RESERVED,
  type McpBuildOptions,
  operationZodShape,
  type ServedResource,
} from "@anvil/mcp-runtime";

/** The generated `mcp/server.js` entrypoint that boots the server over stdio. */
export function generateMcpServerSource(air: AirDocument): string {
  return `#!/usr/bin/env node
// Generated by Anvil for service "${air.service.id}". Do not edit by hand —
// regenerate from AIR (\`anvil compile\`). One tool per approved operation.
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { StdioServerTransport } from "@modelcontextprotocol/sdk/server/stdio.js";
import { buildMcpServer } from "@anvil/mcp-runtime";
import {
  allowedHostsFor,
  FetchTransport,
  InMemoryObserver,
  loadRuntimeConfig,
  resolveCredentials,
  resolveLedger,
} from "@anvil/runtime";
import { loadAirDocument } from "@anvil/air";

const air = loadAirDocument(
  JSON.parse(readFileSync(fileURLToPath(new URL("./air.json", import.meta.url)), "utf8")),
);
const resources = JSON.parse(
  readFileSync(fileURLToPath(new URL("./resources.json", import.meta.url)), "utf8"),
);
const config = loadRuntimeConfig();
const transport = new FetchTransport();
const credentials = resolveCredentials(config);
const ledger = resolveLedger(config.ledger, {
  resultTtlMs: config.ledgerResultTtlSeconds * 1000,
});
const observer = new InMemoryObserver();

// ANVIL_BASE_URL is a deliberate operator override (loopback self-test, staging
// smoke tests); when set without an explicit allowlist, egress pins to its host.
const baseUrl = process.env.ANVIL_BASE_URL ?? air.service.servers[0]?.url ?? "";
const allowedHosts = allowedHostsFor(
  config.allowedHosts,
  baseUrl,
  process.env.ANVIL_BASE_URL !== undefined,
);
const server = buildMcpServer(air, {
  resources,
  contextFor: () => ({
    transport,
    serviceId: air.service.id,
    credentials,
    ledger,
    observer,
    baseUrl,
    authProfile: config.authProfile,
    allowedHosts,
    env: config.env,
  }),
});

await server.connect(new StdioServerTransport());
`;
}

/**
 * The generated `mcp/server-sse.js` entrypoint: the REMOTE MCP server, spoken
 * over HTTP + Server-Sent Events. This is the companion to the stdio server —
 * same tools, same safety runtime — for clients that connect to a URL instead of
 * spawning a local process. It exposes `GET /sse` (the event stream) and
 * `POST /messages?sessionId=…` (client → server), the MCP HTTP+SSE transport.
 *
 * Unlike `runtime/server.js` (stateless StreamableHTTP for Cloud Run), SSE is
 * stateful — one open stream per session — so a horizontally-scaled deployment
 * needs sticky routing by `sessionId`. The safety hot path is identical: one tool
 * per approved operation, egress pinned to the allowlist, secrets from the env.
 */
export function generateMcpSseServerSource(air: AirDocument): string {
  const requiresDurableLedger = air.operations.some(
    (operation) =>
      operation.state === "approved" &&
      operation.effect.kind === "mutation" &&
      operation.idempotency.mode === "required",
  );
  return `#!/usr/bin/env node
// Generated by Anvil for service "${air.service.id}" — REMOTE MCP server (HTTP + SSE).
// Do not edit by hand — regenerate from AIR (\`anvil compile\`).
// Transport: GET /sse (event stream) + POST /messages?sessionId=… (client→server).
import { createServer } from "node:http";
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { SSEServerTransport } from "@modelcontextprotocol/sdk/server/sse.js";
import { buildMcpServer } from "@anvil/mcp-runtime";
import {
  allowedHostsFor,
  FetchTransport,
  InMemoryObserver,
  loadRuntimeConfig,
  probeLedgerReadiness,
  resolveCredentials,
  resolveLedger,
} from "@anvil/runtime";
import { loadAirDocument } from "@anvil/air";

const air = loadAirDocument(
  JSON.parse(readFileSync(fileURLToPath(new URL("./air.json", import.meta.url)), "utf8")),
);
const resources = JSON.parse(
  readFileSync(fileURLToPath(new URL("./resources.json", import.meta.url)), "utf8"),
);
const config = loadRuntimeConfig();
const deps = {
  transport: new FetchTransport(),
  credentials: resolveCredentials(config),
  ledger: resolveLedger(config.ledger, {
    resultTtlMs: config.ledgerResultTtlSeconds * 1000,
  }),
  observer: new InMemoryObserver(),
};
const baseUrl = process.env.ANVIL_BASE_URL ?? air.service.servers[0]?.url ?? "";
const allowedHosts = allowedHostsFor(
  config.allowedHosts,
  baseUrl,
  process.env.ANVIL_BASE_URL !== undefined,
);
const mcpContext = () => ({
  ...deps,
  serviceId: air.service.id,
  baseUrl,
  authProfile: config.authProfile,
  allowedHosts,
  env: config.env,
});

// One live SSE transport per session, keyed by its sessionId. POST /messages
// carries ?sessionId=… back to the stream that owns the conversation.
const sessions = new Map();

function send(res, status, body) {
  res.writeHead(status, { "content-type": "application/json" });
  res.end(JSON.stringify(body));
}

const server = createServer(async (req, res) => {
  const url = new URL(req.url ?? "/", "http://localhost");
  if (url.pathname === "/healthz") return send(res, 200, { status: "ok" });
  if (url.pathname === "/readyz") {
    const ledger = await probeLedgerReadiness(
      deps.ledger,
      ${requiresDurableLedger} && config.env !== "dev",
    );
    return ledger.ready
      ? send(res, 200, { ready: true, service: air.service.id })
      : send(res, 503, {
          ready: false,
          service: air.service.id,
          code: "ledger_unavailable",
        });
  }
  if (req.method === "GET" && url.pathname === "/sse") {
    const transport = new SSEServerTransport("/messages", res);
    sessions.set(transport.sessionId, transport);
    res.on("close", () => sessions.delete(transport.sessionId));
    const mcp = buildMcpServer(air, { resources, contextFor: () => mcpContext() });
    await mcp.connect(transport);
    return;
  }
  if (req.method === "POST" && url.pathname === "/messages") {
    const sessionId = url.searchParams.get("sessionId");
    const transport = sessionId ? sessions.get(sessionId) : undefined;
    if (!transport)
      return send(res, 400, {
        error: { code: "no_session", message: "Unknown or missing sessionId. Open GET /sse first." },
      });
    return transport.handlePostMessage(req, res);
  }
  return send(res, 404, {
    error: { code: "not_found", message: "MCP over SSE: GET /sse, then POST /messages?sessionId=…" },
  });
});

server.listen(process.env.PORT ?? 8080, () =>
  console.error(air.service.id, "MCP SSE listening on /sse"),
);
`;
}
